char	*ft_strrev(char *str)
{
	int		i;
	int		j;
	char	t;

	i = 0;
	j = 0;
	while (str[j])
		j++;
	j--;
	while (i <= j)
	{
		t = str[i];
		str[i] = str[j];
		str[j] = t;
		j--;
		i++;
	}
	return (str);
}
