int		ft_sqrt(int nb)
{
	int temp;

	temp = 4;
	if (nb <= 0 || nb == 2 || nb == 3)
		return (0);
	if (nb == 1)
		return (1);
	while (temp <= nb / temp)
	{
		if (temp == nb / temp)
			return (temp);
		temp++;
	}
	return (0);
}
